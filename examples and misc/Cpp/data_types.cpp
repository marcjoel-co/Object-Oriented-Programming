#include <iostream>

int main() {
    int an_integer = 10;
    float a_float = 10.5f;
    char a_character = 'A';

    std::cout << "Integer: " << an_integer << std::endl;
    std::cout << "Float: " << a_float << std::endl;
    std::cout << "Character: " << a_character << std::endl;

    return 0;
}
