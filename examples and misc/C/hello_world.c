// The most basic C program, prints "Hello, World!" to the console.

#include <stdio.h>

int main() { 
    printf("Hello, World!\n");
    return 0;
}

/*
// Definitions
 - Console: A text-based interface to interact with the computer.
 - printf: A function that prints a formatted string to the console. 
 - main: The entry point of a C program, where execution begins.
    - This is where our program starts running.
    - { }: Curly braces that define a block of code.
    - int: A data type that represents integers.
    - return: A statement that exits the main function and returns a value to the operating system.
    - return 0: Indicates successful program execution.
*/