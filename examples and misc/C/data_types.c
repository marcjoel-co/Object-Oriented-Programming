#include <stdio.h>

int main() { 
    int an_integer = 10;   // takes 4 bytes of memory
    float a_float = 10.5;  // takes also 4 bytes of memory
    char a_character = 'A'; // a char takes 1 byte of memory

    printf("Integer: %d\n", an_integer);
    printf("Float: %.2f\n", a_float);
    printf("Character: %c\n", a_character);

    return 0;
}

