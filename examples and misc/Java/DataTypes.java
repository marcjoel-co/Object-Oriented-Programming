public class DataTypes {
    public static void main(String[] args) {
        int anInteger = 10;
        float aFloat = 10.5f;
        char aCharacter = 'A';

        System.out.println("Integer: " + anInteger);
        System.out.println("Float: " + aFloat);
        System.out.println("Character: " + aCharacter);
    }
}
