#include <stdio.h>

int main() {
    int a = 10, b = 20; // initialize two integer variables
    int sum = a + b;    // add the two variables
    int diff = a - b;   // subtract the two variables
    int product = a * b; // multiply the two variables
    int quotient = a / b; // divide the two variables

    printf("Sum: %d\n", sum);
    printf("Difference: %d\n", diff);
    printf("Product: %d\n", product);
    printf("Quotient: %d\n", quotient);

    return 0;
}
